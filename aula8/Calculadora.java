package aula8;

public class Calculadora {
    int somar(int a, int b){
        return a + b;
    }
    int multiplicar(int a, int b){
        return a * b;
    }
    float dividir(float a , float b){
        return a / b;
    }
    int subtrair(int a, int b){
        return a -b;
    }
}
